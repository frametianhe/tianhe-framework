package com.tianhe.framework.designpatterns.study.builder;

public class Hourse {

	// 地板
	private String floor;
	// 墙
	private String wall;
	// 屋顶
	private String hourseTop;

	public String getFloor() {
		return floor;
	}

	public void setFloor(String floor) {
		this.floor = floor;
	}

	public String getWall() {
		return wall;
	}

	public void setWall(String wall) {
		this.wall = wall;
	}

	public String getHourseTop() {
		return hourseTop;
	}

	public void setHourseTop(String hourseTop) {
		this.hourseTop = hourseTop;
	}

}
